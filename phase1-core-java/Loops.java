public class Loops {
    public static void main(String[] args)
    {
        //for 
        System.out.println("for\n");
         for(int i=0;i<=10;i++)
         {
            System.out.println(i);
         }

         //while

         System.out.println("While\n");
         int i=0;
         while(i<=10)
         {
            System.out.println(i);
            i++;
         }

         //do while
         System.out.println("Do While\n");
         do{
            System.out.println(i);
            i++;
         }while(i<=10);
    }
    
}
